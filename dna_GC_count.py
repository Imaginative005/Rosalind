    "def gc_content(dna_string):\n",
    "    \"\"\"\n",
    "    Calculates the GC-content of a DNA string.\n",
    "    \"\"\"\n",
    "    count = 0\n",
    "    for symbol in dna_string:\n",
    "        if symbol == 'G' or symbol == 'C':\n",
    "            count += 1\n",
    "    return (count / len(dna_string)) * 100\n",
